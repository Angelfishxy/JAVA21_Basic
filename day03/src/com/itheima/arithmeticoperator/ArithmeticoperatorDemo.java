package com.itheima.arithmeticoperator;

public class ArithmeticoperatorDemo {
    public static void main(String[] args) {
        //add
        System.out.println(3 + 2);
        //sub
        System.out.println(5 - 1);
        //multiply
        System.out.println(3 * 2);
        //divide
        System.out.println(6 / 2);
        //mod
        System.out.println(6 % 4);
    }
}
